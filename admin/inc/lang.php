    global $translations;
    $lang = getenv('LANG');
    if ($echo) {
        echo isset($translations[$lang][$string]) ? $translations[$lang][$string] : $string;
    }
    else {
        return isset($translations[$lang][$string]) ? $translations[$lang][$string] : $string;
    }
    
}

